class User {
    #fName;
    #lName;
    #email;
    #userName;
    #password;

    constructor(fName, lName, email, userName, password) {
        this.fName = fName;
        this.lName = lName;
        this.email = email;
        this.userName = userName;
        this.password = password;

    }
}

export default User;
